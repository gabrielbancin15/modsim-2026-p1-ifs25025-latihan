# aritmatic operations
number = 1 + 2 * 3 / 4.0
print(number)

# modulo operation
remainder = 11 % 3
print(remainder)

# multiple symbols
squared = 7 ** 2
cubed = 2 ** 3
print(squared)
print(cubed)

# operators with strings
helloworld = "hello" + " " + "world"
print(helloworld)

# repeating strings
lotsofhellos = "hello" * 10
print(lotsofhellos)

# operators with lists
even_numbers = [2, 4, 6, 8]
more_even_numbers = [10, 12, 14, 16]
all_even_numbers = even_numbers + more_even_numbers
print(all_even_numbers)

# repeating lists
print([1, 2, 3] * 3)

